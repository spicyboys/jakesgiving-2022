<div class="justify-self-center sm:w-[500px] w-10/12 bg-[#F4EBD0] m-10 p-4">
	<h2>What <i>is</i> Jakesgiving?</h2>
	<p>
		Jakesgiving is an annual Friendsgiving in Austin started by Jacob in 2012 to celebrate
		Thanksgiving with all our friends before everyone flies out for Turkey Day proper.
	</p>
	<p>No, he did not pick the name. Yes, it stuck.</p>
	<hr />

	<h2>Do I need to bring anything?</h2>
	<p>
		Nothing but your standard-issue holiday cheer! Jakesgiving is a free, fully prepared dinner with
		enough food and drink for all.
	</p>
	<p>
		However if there's something delicious you simply <i>must</i> bring, it's not like I can stop you.
		I'm an FAQ, not a cop. Just know that if it requires oven time, it's not getting it.
	</p>
	<hr />

	<h2>Anything COVID-related I should know about?</h2>
	<p>
		Jakesgiving will take place between both indoor and outdoor spaces, with masks considered
		optional.
	</p>
	<p>
		While we've taken what precautions we can to reduce risk, there's simply not enough space to
		ensure proper social distancing. Therefore, for the safety of all involved and also to keep this
		party from making the news as the hottest new pre-Thanksgiving super-spreader event in Austin,
		all guests must be fully vaccinated.
	</p>
	<p>
		If you are unable to be vaccinated, think you might have been exposed, or are experiencing any
		symptoms, we ask you stay home this year. (We'll be sure to save some extra just for you.)
	</p>
	<hr />

	<h2>What's the parking situation like?</h2>
	<p>
		There's generally plenty of free parking on Durwood by the park on both sides of the street. We
		expect you'll be able to find a convenient parking spot within easy walking distance.
	</p>
	<hr />

	<h2>Can I bring my pet?</h2>
	<p>
		Given the large number of people and the nice-once-you-get-to-know-them house cats, we kindly
		ask you leave your furry/Furry friends at home.
	</p>
	<p>(Oliver gets grandfathered in, however. It's in his lease.)</p>
	<hr />

	<h2>Are you <i>sure</i> I can't contribute somehow?</h2>
	<p>
		We're sure, but we really appreciate it! In fact, part of the Jakesgiving tradition is finding
		out just how much extra food we accidentally cooked.
	</p>
	<p>
		However, if you're one of those who just can't sleep at night until you've tossed coins into
		<i>someone's</i> hat, you can Venmo
		<a href="https://venmo.com/u/bobertforever">@bobertforever</a> to help cover the cost of food
		and drinks. Again this is <b>entirely optional</b>, but always appreciated.
	</p>
	<hr />

	<h2>What if I have dietary restrictions?</h2>
	<p>
		Just let us know in the comments of <a href="/rsvp" class="link">your RSVP</a> and we'll work
		something out. We've also listed dietary notes for each dish in the
		<a href="/menu" class="link">menu</a>.
	</p>
	<hr />

	<h2>Additional questions?</h2>
	<p>Please reach out to Robert, Jacob, or Nich and they'll get you sorted.</p>
</div>

